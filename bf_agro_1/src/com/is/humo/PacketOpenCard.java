package com.is.humo;

public class PacketOpenCard {

	String cardType;
	String cardTypeNamber;
	String branch;
	String clientB;
	String account;
	String rangeId;
	String branchId;
	
	
	public String getCardType() {
		return cardType;
	}
	public void setCardType(String cardType) {
		this.cardType = cardType;
	}
	public String getCardTypeNamber() {
		return cardTypeNamber;
	}
	public void setCardTypeNamber(String cardTypeNamber) {
		this.cardTypeNamber = cardTypeNamber;
	}
	public String getBranch() {
		return branch;
	}
	public void setBranch(String branch) {
		this.branch = branch;
	}
	public String getClientB() {
		return clientB;
	}
	public void setClientB(String clientB) {
		this.clientB = clientB;
	}
	public String getAccount() {
		return account;
	}
	public void setAccount(String account) {
		this.account = account;
	}
	public String getRangeId() {
		return rangeId;
	}
	public void setRangeId(String rangeId) {
		this.rangeId = rangeId;
	}
	public String getBranchId() {
		return branchId;
	}
	public void setBranchId(String branchId) {
		this.branchId = branchId;
	}
	
	
}
