package com.is.bpri;

public class BproductClientsModel {
	
	private String name;
	private String passport;
	private String number_tax_registration;
	private String inps;
	private String phone_home;
	private String phone_mobile;
	private String email_address;
	private String fax;
	
	public String getName() {
		return name;
	}
	
	public void setName(String name) {
		this.name = name;
	}
	
	public String getPassport() {
		return passport;
	}
	
	public void setPassport(String passport) {
		this.passport = passport;
	}
	
	public String getNumber_tax_registration() {
		return number_tax_registration;
	}
	
	public void setNumber_tax_registration(String number_tax_registration) {
		this.number_tax_registration = number_tax_registration;
	}
	
	public String getInps() {
		return inps;
	}
	
	public void setInps(String inps) {
		this.inps = inps;
	}
	
	public String getPhone_home() {
		return phone_home;
	}
	
	public void setPhone_home(String phone_home) {
		this.phone_home = phone_home;
	}
	
	public String getPhone_mobile() {
		return phone_mobile;
	}
	
	public void setPhone_mobile(String phone_mobile) {
		this.phone_mobile = phone_mobile;
	}
	
	public String getEmail_address() {
		return email_address;
	}
	
	public void setEmail_address(String email_address) {
		this.email_address = email_address;
	}

	public String getFax() {
		return fax;
	}

	public void setFax(String fax) {
		this.fax = fax;
	}
	
}
