package com.is.humo;

public class HumoCardSetting {

	private String code;
	private String name;
	private String code_b;
	private String code_k;
	
	public String getCode() {
		return code;
	}
	public void setCode(String code) {
		this.code = code;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getCode_b() {
		return code_b;
	}
	public void setCode_b(String code_b) {
		this.code_b = code_b;
	}
	public String getCode_k() {
		return code_k;
	}
	public void setCode_k(String code_k) {
		this.code_k = code_k;
	}
	
	
	
	
}
