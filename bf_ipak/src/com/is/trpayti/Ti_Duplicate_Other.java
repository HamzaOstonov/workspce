package com.is.trpayti;

public class Ti_Duplicate_Other {

	private int id;
	private String branch;
	private String report_file;
	private String name2;
	private String my_branch;
	private String inclient_name1;
	private String v_date;
	private int deal_id;
	private int act_id;
	
	public Ti_Duplicate_Other() {
		super();
	}

	public Ti_Duplicate_Other(int id, String branch, String report_file, String name2, String my_branch,
			String inclient_name1, String v_date, int deal_id, int act_id) {
		super();
		this.id = id;
		this.branch = branch;
		this.report_file = report_file;
		this.name2 = name2;
		this.my_branch = my_branch;
		this.inclient_name1 = inclient_name1;
		this.v_date = v_date;
		this.deal_id = deal_id;
		this.act_id = act_id;
	}

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getBranch() {
		return branch;
	}

	public void setBranch(String branch) {
		this.branch = branch;
	}

	public String getReport_file() {
		return report_file;
	}

	public void setReport_file(String report_file) {
		this.report_file = report_file;
	}

	public String getName2() {
		return name2;
	}

	public void setName2(String name2) {
		this.name2 = name2;
	}

	public String getMy_branch() {
		return my_branch;
	}

	public void setMy_branch(String my_branch) {
		this.my_branch = my_branch;
	}

	public String getInclient_name1() {
		return inclient_name1;
	}

	public void setInclient_name1(String inclient_name1) {
		this.inclient_name1 = inclient_name1;
	}

	public String getV_date() {
		return v_date;
	}

	public void setV_date(String v_date) {
		this.v_date = v_date;
	}

	public int getDeal_id() {
		return deal_id;
	}

	public void setDeal_id(int deal_id) {
		this.deal_id = deal_id;
	}

	public int getAct_id() {
		return act_id;
	}

	public void setAct_id(int act_id) {
		this.act_id = act_id;
	}
	
	
	
	
}
