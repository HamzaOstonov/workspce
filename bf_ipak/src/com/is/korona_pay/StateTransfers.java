package com.is.korona_pay;

public class StateTransfers {

	public String UIN;
	public String ReceiverFullName;
	public String SenderFullNAme;
	public int StateTransfers;
	public int AmountSumma;
	
	public StateTransfers() {
		
	}

	
	public String getUIN() {
		return UIN;
	}

	
	public void setUIN(String uIN) {
		UIN = uIN;
	}


	public String getReceiverFullName() {
		return ReceiverFullName;
	}

	
	public void setReceiverFullName(String receiverFullName) {
		ReceiverFullName = receiverFullName;
	}

	public String getSenderFullNAme() {
		return SenderFullNAme;
	}


	public void setSenderFullNAme(String senderFullNAme) {
		SenderFullNAme = senderFullNAme;
	}


	public int getStateTransfers() {
		return StateTransfers;
	}


	public void setStateTransfers(int stateTransfers) {
		StateTransfers = stateTransfers;
	}


	public int getAmountSumma() {
		return AmountSumma;
	}

	public void setAmountSumma(int amountSumma) {
		AmountSumma = amountSumma;
	}
	
	

}
